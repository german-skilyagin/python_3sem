n = int(input()) # число записей в исходном списке
a = list(map(int, input().split())) # список номеров книг

# Сортируем список по возрастанию
a.sort()

# Создаем пустой список для результата
result = []

# Инициализируем начало и конец текущего интервала
start = a[0]
end = a[0]

# Проходим по всем элементам списка
for i in range(1, n):
    # Если текущий элемент на единицу больше предыдущего
    if a[i] == a[i-1] + 1:
        # Продолжаем интервал
        end = a[i]
    else:
        # Иначе добавляем интервал в результат
        if start == end:
            # Если интервал состоит из одного элемента
            result.append(str(start))
        else:
            # Если интервал состоит из нескольких элементов
            result.append(str(start) + ' ... ' + str(end))
        # Начинаем новый интервал
        start = a[i]
        end = a[i]

# Добавляем последний интервал в результат
if start == end:
    result.append(str(start))
else:
    result.append(str(start) + ' ... ' + str(end))

# Выводим результат через пробел
print(' '.join(result))